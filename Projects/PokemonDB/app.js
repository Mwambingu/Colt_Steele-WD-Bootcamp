const pokemons = [];
const pokemonImgLink =
    "https://assets.pokemon.com/assets/cms2/img/pokedex/full/";

for (let name of pokemonNamesData) {
    let pokemonInfo = {};
    let pokemonIndex = pokemonNamesData.indexOf(name) + 1;
    pokemonInfo.name = name;
    if (pokemonIndex < 10) {
        pokemonInfo.link =
            pokemonInfo.link = `${pokemonImgLink}00${pokemonIndex}.png`;
    } else if (pokemonIndex < 100) {
        pokemonInfo.link =
            pokemonInfo.link = `${pokemonImgLink}0${pokemonIndex}.png`;
    } else {
        pokemonInfo.link = `${pokemonImgLink}${pokemonIndex}.png`;
    }
    pokemons.push(pokemonInfo);
}

console.log(pokemons);

const pokemonContainer = document.querySelector(".pokemon-container");

console.log(pokemonContainer);

for (let pokemon of pokemons) {
    let pokemonCard = document.createElement("div");
    let pokemonImg = document.createElement("img");
    let pokemonName = document.createElement("span");
    let pokemonNameDiv = document.createElement("div");
    pokemonCard.classList.add("pokemon-card");
    pokemonImg.src = pokemon.link;
    pokemonImg.classList.add("pokemon-img");
    pokemonName.innerText = pokemon.name;
    pokemonName.classList.add("pokemon-name");
    pokemonNameDiv.classList.add("pokemon-name-container");
    pokemonContainer.append(pokemonCard);
    pokemonCard.appendChild(pokemonImg);
    pokemonCard.appendChild(pokemonNameDiv);
    pokemonNameDiv.appendChild(pokemonName);
}
